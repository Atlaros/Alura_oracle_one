
public class EjemploCiclosWhile {
	
	public static void main(String[] args) {
		
		int contador = 0;
		
		while(contador <= 10) { // mientras que (condicion) sea verdadero ejecuta esto.
			//Ejecuta esto
			System.out.println(contador);
			// contador = contador + 1; le asignamos un valor contaor que es el mismo mas 1
			// contador += 3; esta exprecion dice imcremetnamen en esta cantidad
			contador++;// para incrementar el contador de uno en uno
		}
		
	}
}

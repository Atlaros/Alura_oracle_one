
public class ejemploValoresVariables {

    public static void main(String[] args) {
        int numero1 = 5;
        int numero2 = 9;
        System.out.println(numero2);
        numero2 = numero1;
        System.out.println(numero2);
        numero1 = 88;
        System.out.println(numero2);
        //las variables de java guarda valor, no como otros lenguajes que guarda punteros.
    }
}